
public class Else_If_2 {
public static void main(String[] args) {
	    
	    double x=8;
	    double y=10;
	    double z=12;
	    
	    
	    if (x<y && x==0)
	    {
	        System.out.println("If statement");
	    }
	    else if(x>z || x!=0)
	    {
	    	System.out.println("Else If statement");
	    }
	    else 
	    {
	    	System.out.println("Else Statement");
	    }

}
}
